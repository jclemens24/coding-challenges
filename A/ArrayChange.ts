const ArrayChange = function (inputArray: number[]): number {
  let sum: number = 0;

  for (let i = 1; i < inputArray.length; i++) {
    const difference = inputArray[i - 1] - inputArray[i] + 1;
    if (difference > 0) {
      sum += difference;
      inputArray[i] += difference;
      console.log(inputArray);
    }
  }
  return sum;
};

